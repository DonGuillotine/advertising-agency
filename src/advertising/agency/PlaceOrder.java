    // Filtering Adverts by Category
    public void advertNameByCategory(String category){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Product> list = ProductDao.getAllRecordsByCategory(category);
        Iterator<Product> itr = list.iterator();
        while(itr.hasNext()){
            Product productobj = itr.next();
            dtm.addRow(new Object[]{productobj.getName()});
        }
   }
    
    // Filtering Adverts by Name
    public void filterAdvertByName(String name, String category){
    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
    dtm.setRowCount(0);
    ArrayList<Product> list = ProductDao.filterAdvertsByName(name, category);
    Iterator<Product> itr = list.iterator();
    while(itr.hasNext()){
        Product productobj = itr.next();
        dtm.addRow(new Object[]{productobj.getName()});
        }
    }
    
    
